<h2>Montjoie</h2>
<p>Montjoie is a C++ oriented object code, mainly written by <a href="http://www.math.u-bordeaux1.fr/~durufle/">Marc Durufl&eacute;</a>.
  Montjoie is designed for the efficient solution of time-domain and time-harmonic linear partial differential equations using high-order finite element methods.
  This code is mainly written for quadrilateral (in 2D) / hexahedral (in 3D) finite elements, partial implementations of triangular (in 2D) / tetrahedral (in 3D) 
  elements are provided. The equations solved by this code come from the &quot;wave propagation&quot; problems, particularly acoustic, electromagnetic,
  aeroacoustic, elastodynamic problems.</p>
<p>Montjoie homepage: <a href="http://gforge.inria.fr/projects/montjoie/">http://gforge.inria.fr/projects/montjoie/</a></p>
<h2>EF Toolbox</h2>
<p>EF Toolbox is a collection of Matlab tools, written by <a href="http://www.cmap.polytechnique.fr/~alouges/">Fran&ccedil;ois Alouges</a> and <a href="mailto:marc.tajchman@cea.fr">Marc Tajchman</a>. It contains the triangular mesh generator <a href="http://www.cs.cmu.edu/~quake/triangle.html">Triangle</a> written by <a href="http://www.cs.berkeley.edu/~jrs">Jonathan Richard
  Shewchuk</a>, two overlays to be used directly in Matlab, and some Matlab runtimes to build rigidity and mass matrices. Currently
  implemented elements are P0, P1, P2, P1-bubble and non-conforming P1 elements. </p>
